import wx

##############################################################################
class MainFrame(wx.Frame):
    def __init__(self, title = 'GUI', size = (1200, 800)):
        wx.Frame.__init__(self, parent=None, id=-1, title=title, size=size)
        self.DoLayout()
        
    #-------------------------------------------------------------------------
    def DoLayout(self):
        self.Layout()
    
